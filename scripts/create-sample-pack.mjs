// ─── create-sample-pack.mjs ──────────────────────────────────────────────────
// Generates a signed sample PMEGP Update Pack for use as a test fixture.
//
// Outputs (in `tests/fixtures/sample-update-pack/`):
//   • manifest.json  — signed manifest (version, schemeCode, files, signature,
//                       publicKeyId). Verified by `verifyAndApply` in the
//                       update-engine.
//   • pack.json      — the actual update payload: `{ knowledge_version, data? }`.
//                       SHA-256 of this file's bytes is recorded in
//                       `manifest.files[0].sha256`.
//
// Usage:
//   bun scripts/create-sample-pack.mjs [--out <dir>] [--version <ver>]
//
// Defaults: out = tests/fixtures/sample-update-pack, version = 2026.01.15-sample.
//
// The manifest is signed with `.secrets/update-signing-private-key.pem` and is
// verifiable against the public key bundled in
// `src/engines/update-engine/public-key.pem` (both produced by the keygen step
// in this task). This makes the fixture usable in an end-to-end test that
// invokes the engine's `verifyAndApply` against the bundled public key.
// ─────────────────────────────────────────────────────────────────────────────

import { writeFileSync, readFileSync, mkdirSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";
import crypto from "node:crypto";
import { signManifest, canonicalBytesForSignature } from "./sign-manifest.mjs";

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const PROJECT_ROOT = resolve(__dirname, "..");

// ── CLI parsing ──────────────────────────────────────────────────────────────

function parseArgs(argv) {
  const args = argv.slice(2);
  /** @type {string | undefined} */
  let outDir;
  /** @type {string | undefined} */
  let version;
  for (let i = 0; i < args.length; i++) {
    const a = args[i];
    if (a === "--out") {
      outDir = args[++i];
      if (!outDir) {
        console.error("Error: --out requires a directory argument");
        process.exit(2);
      }
    } else if (a === "--version") {
      version = args[++i];
      if (!version) {
        console.error("Error: --version requires a value argument");
        process.exit(2);
      }
    } else if (a === "--help" || a === "-h") {
      console.error(
        "Usage: bun scripts/create-sample-pack.mjs [--out <dir>] [--version <ver>]"
      );
      process.exit(0);
    } else {
      console.error(`Error: unexpected argument: ${a}`);
      process.exit(2);
    }
  }
  return {
    outDir: outDir ?? "tests/fixtures/sample-update-pack",
    version: version ?? "2026.01.15-sample",
  };
}

// ── Helpers ──────────────────────────────────────────────────────────────────

/** SHA-256 hex digest of a UTF-8 string (matches the engine's `sha256Hex`). */
function sha256HexUtf8(text) {
  return crypto.createHash("sha256").update(text, "utf8").digest("hex");
}

// ── Main ─────────────────────────────────────────────────────────────────────

const { outDir: outDirRel, version } = parseArgs(process.argv);
const outDir = resolve(PROJECT_ROOT, outDirRel);
mkdirSync(outDir, { recursive: true });

// ── 1. Build the pack payload (PackPayload in the engine) ────────────────────
// The engine parses this as JSON and upserts `knowledge_version` + every entry
// of `data` into the `app_meta` table inside a single transaction. Values are
// JSON-serialized before storage (TEXT column), so non-string values exercise
// that coercion path — included deliberately.
const packPayload = {
  knowledge_version: version,
  data: {
    last_knowledge_refresh: "2026-01-15T00:00:00.000Z",
    nic_code_count: 1234,
    machinery_catalog_count: 567,
    sample_update_note: "Synthetic fixture pack generated by scripts/create-sample-pack.mjs",
  },
};

// Canonical pack bytes: stable JSON with sorted keys + 2-space indent for
// human readability. The manifest only references the SHA-256 of these bytes,
// so formatting is free-form — but a stable format keeps the fixture diff-clean
// across regenerations with identical inputs.
const packJson = JSON.stringify(packPayload, null, 2) + "\n";
const packPath = resolve(outDir, "pack.json");
writeFileSync(packPath, packJson, "utf8");

const packSha256 = sha256HexUtf8(packJson);

// ── 2. Build the manifest (without signature first) ──────────────────────────
// Shape MUST match `DataPackManifest` in src/engines/update-engine/index.ts:
//   { version, schemeCode: "PMEGP", files: [{ path, sha256 }], signature, publicKeyId }
const manifest = {
  version,
  schemeCode: "PMEGP",
  files: [
    {
      path: "pack.json",
      sha256: packSha256,
    },
  ],
  // `signature` is intentionally omitted here — `signManifest` strips it
  // before canonicalizing anyway, but omitting it makes the "unsigned
  // manifest" state explicit.
  publicKeyId: "v1",
};

// ── 3. Sign the manifest with the project Ed25519 private key ────────────────
const privKeyPath = resolve(PROJECT_ROOT, ".secrets", "update-signing-private-key.pem");
let privKeyPem;
try {
  privKeyPem = readFileSync(privKeyPath, "utf8");
} catch {
  console.error(
    `Error: private key not found at ${privKeyPath}.\n` +
      "Generate it with the keygen snippet in docs/UPDATE_SIGNING.md first."
  );
  process.exit(1);
}

const signature = signManifest(manifest, privKeyPem);
manifest.signature = signature;

const manifestPath = resolve(outDir, "manifest.json");
writeFileSync(manifestPath, JSON.stringify(manifest, null, 2) + "\n", "utf8");

// ── 4. Self-verify with the bundled public key (round-trip sanity check) ────
// Mirrors what `verifyAndApply` does: strip signature → canonical JSON →
// verifyAsync(sigBytes, messageBytes, rawPubKey). The raw 32-byte key is
// extracted from the SPKI PEM the same way the engine does it.
const pubPemPath = resolve(
  PROJECT_ROOT,
  "src",
  "engines",
  "update-engine",
  "public-key.pem"
);
const pubPem = readFileSync(pubPemPath, "utf8");
const pubBody = pubPem
  .replace(/-----BEGIN [^-]+-----/g, "")
  .replace(/-----END [^-]+-----/g, "")
  .replace(/\s+/g, "");
const pubDer = Buffer.from(pubBody, "base64");
// SPKI Ed25519 = 44 bytes: 12-byte algo-identifier prefix + 32-byte raw key.
// The engine falls back to a useless zero key if this doesn't match — but for
// the bundled real key it always will.
const SPKI_PREFIX = Buffer.from([
  0x30, 0x2a, 0x30, 0x05, 0x06, 0x03, 0x2b, 0x65, 0x70, 0x03, 0x21, 0x00,
]);
let rawPubKey;
if (pubDer.length === 32) {
  rawPubKey = pubDer;
} else if (pubDer.length === 44 && pubDer.subarray(0, 12).equals(SPKI_PREFIX)) {
  rawPubKey = pubDer.subarray(12);
} else {
  console.error(
    `Error: bundled public-key.pem is neither a 32-byte raw key nor a ` +
      `44-byte SPKI Ed25519 key (got ${pubDer.length} bytes). ` +
      `Run the keygen snippet to regenerate.`
  );
  process.exit(1);
}

const pubKey = crypto.createPublicKey(pubPem);
const sigBytes = Buffer.from(signature, "base64");
// Recompute canonical bytes the same way the signer did (signature stripped).
const messageBytes = canonicalBytesForSignature(manifest);
const verified = crypto.verify(null, messageBytes, pubKey, sigBytes);

// ── 5. Report ────────────────────────────────────────────────────────────────
console.log("Sample update pack generated:");
console.log(`  out dir:        ${outDir}`);
console.log(`  manifest.json:  ${manifestPath}`);
console.log(`  pack.json:      ${packPath}`);
console.log("");
console.log("Manifest:");
console.log(`  version:        ${manifest.version}`);
console.log(`  schemeCode:     ${manifest.schemeCode}`);
console.log(`  publicKeyId:    ${manifest.publicKeyId}`);
console.log(`  files[0].path:  ${manifest.files[0].path}`);
console.log(`  files[0].sha256: ${manifest.files[0].sha256}`);
console.log(`  signature:      ${manifest.signature} (${sigBytes.length} bytes raw)`);
console.log("");
console.log(`Self-verify against bundled public key: ${verified ? "PASS ✓" : "FAIL ✗"}`);
if (!verified) {
  console.error("Self-verification failed — the fixture will not pass `verifyAndApply`.");
  process.exit(1);
}
